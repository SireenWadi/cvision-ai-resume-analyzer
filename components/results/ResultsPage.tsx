interface Props { data: AnalysisData; navigate: (p: Page) => void; onNewUpload: () => void }

function ScoreRing({ score, color, size = 80 }: { score: number; color: string; size?: number }) {
  const deg = (score / 100) * 360
  const inner = size * 0.78
  return (
    <div style={{ width: size, height: size, borderRadius: '50%', background: `conic-gradient(${color} ${deg}deg, rgba(255,255,255,0.05) 0deg)`, display: 'flex', alignItems: 'center', justifyContent: 'center', margin: '0 auto', boxShadow: `0 0 25px ${color}30` }}>
      <div style={{ width: inner, height: inner, borderRadius: '50%', background: 'rgba(6,8,18,0.92)', display: 'flex', flexDirection: 'column', alignItems: 'center', justifyContent: 'center', fontFamily: 'Outfit,sans-serif', fontWeight: 800, fontSize: size > 100 ? 32 : size > 80 ? 22 : 18, color, lineHeight: 1 }}>
        {score}<small style={{ fontSize: size > 80 ? 10 : 8, fontWeight: 500, color: 'var(--text-muted)' }}>/ 100</small>
      </div>
    </div>
  )
}

function getScoreColor(score: number) { return score >= 80 ? '#4DFFC3' : score >= 60 ? '#FCD34D' : '#FB7185' }
function getScoreLabel(score: number) {
  if (score >= 85) return { text: 'Excellent', color: '#4DFFC3' }
  if (score >= 70) return { text: 'Good',      color: '#4DFFC3' }
  if (score >= 55) return { text: 'Fair',      color: '#FCD34D' }
  return { text: 'Needs Work', color: '#FB7185' }
}

const handleDownloadReport = (data: AnalysisData) => {
  const report = `CVision AI — Resume Analysis Report\n=====================================\nFile: ${data.fileName}\nDate: ${new Date().toLocaleDateString()}\n\nSCORES\n------\nOverall Score:  ${data.overallScore}/100\nATS Score:      ${data.atsScore}/100\nGrammar Score:  ${data.grammarScore}/100\nKeyword Score:  ${data.keywordScore}/100\n\nRESUME STATS\n------------\nWord Count:     ${data.wordCount}\nSections Found: ${data.sectionCount}\n\nSKILLS DETECTED (${data.presentSkills.length})\n${data.presentSkills.join(', ')}\n\nMISSING SKILLS (${data.missingSkills.length})\n${data.missingSkills.slice(0, 15).join(', ')}\n\nIMPROVEMENT SUGGESTIONS\n------------------------\n${data.suggestions.map((s, i) => `${i + 1}. ${s}`).join('\n')}\n\n---\nGenerated by CVision AI`.trim()
  const blob = new Blob([report], { type: 'text/plain' })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a'); a.href = url; a.download = `cvision-report-${data.fileName.replace(/\.[^.]+$/, '')}.txt`; a.click(); URL.revokeObjectURL(url)
}

